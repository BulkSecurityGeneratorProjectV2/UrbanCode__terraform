package com.urbancode.terraform.tasks.vcloud;

public class BadResponse extends Exception {

    //**********************************************************************************************
    // CLASS
    //**********************************************************************************************
    final static private long serialVersionUID = 1L;

    //**********************************************************************************************
    // INSTANCE
    //**********************************************************************************************
    int status;

    //----------------------------------------------------------------------------------------------
    public BadResponse() {
    }
    
    //----------------------------------------------------------------------------------------------
    public BadResponse(int status) {
        this.status = status;
    }
    
    //----------------------------------------------------------------------------------------------
    public BadResponse(String message, Throwable cause) {
        super(message, cause);
    }

    //----------------------------------------------------------------------------------------------
    public BadResponse(String message) {
        super(message);
    }
    
    //----------------------------------------------------------------------------------------------
    public BadResponse(String message, int status) {
        super(message);
        this.status = status;
    }

    //----------------------------------------------------------------------------------------------
    public BadResponse(Throwable cause) {
        super(cause);
    }
    
    //----------------------------------------------------------------------------------------------
    public int getStatus() {
        return status;
    }
}
