package com.urbancode.uprovision.tasks.vmware.util;

public class IpInUseException extends Exception {

    //**********************************************************************************************
    // CLASS
    //**********************************************************************************************
    final static private long serialVersionUID = 1L;

    //**********************************************************************************************
    // INSTANCE
    //**********************************************************************************************

    //----------------------------------------------------------------------------------------------
    public IpInUseException() {
    }

    //----------------------------------------------------------------------------------------------
    public IpInUseException(String message) {
        super(message);
    }

    //----------------------------------------------------------------------------------------------
    public IpInUseException(Throwable cause) {
        super(cause);
    }

    //----------------------------------------------------------------------------------------------
    public IpInUseException(String message, Throwable cause) {
        super(message, cause);
    }
}
